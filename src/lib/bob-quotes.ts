function pick<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)]
}

function getSizeCategory(rawJsonLength: number): SizeCategory {
  if (rawJsonLength < 5_000) return 'tiny'
  if (rawJsonLength < 50_000) return 'small'
  if (rawJsonLength < 500_000) return 'medium'
  return 'heavy'
}

export type BobMood = 'happy' | 'grumpy' | 'angry'

export interface BobReaction {
  quote: string
  mood: BobMood
}

export function getBobQuote(snapshot: Snapshot): BobReaction {
  const diagramCount = snapshot.diagrams?.length ?? 0
  const noiseCount = snapshot.noiseNodes?.length ?? 0

  // Если Боб не нашёл ни одной диаграммы — он счастлив
  if (diagramCount === 0) {
    return { quote: pick(emptyQuotes), mood: 'happy' }
  }

  const size = getSizeCategory(snapshot.rawJson.length)
  const parts: string[] = [pick(sizeQuotes[size])]

  // Добавляем комментарий про подграфы (если > 2)
  if (diagramCount > 2) {
    parts.push(pick(subgraphQuotes).replace('{n}', String(diagramCount)))
  }

  // Добавляем комментарий про мусор (если много)
  if (noiseCount > 10) {
    parts.push(pick(noiseQuotes).replace('{n}', String(noiseCount)))
  }

  const mood: BobMood = size === 'heavy' || size === 'medium' ? 'angry' : 'grumpy'

  return { quote: parts.join(' '), mood }
}
